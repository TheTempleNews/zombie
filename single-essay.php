									<h1 class="single-title" itemprop="headline">Mystery of tiles on city streets solved in Philadelphia documentary</h1>
									
									<p class="byline"><i>by</i> <span class="authors"><a href="http://temple-news.dev/author/patriciamadej/" title="Posts by Patricia Madej" rel="author">Patricia Madej</a></span></p>

									<section class="dek">
										<p>A free screening of the documentary “Resurrect Dead: The Mystery of the Toynbee Tiles” gave students an insight of a unique phenomenon in the city.</p>
									</section> <!-- end dek -->
						
								</header> <!-- end article header -->
					
								<section class="post-content twelvecol first last clearfix" itemprop="articleBody">
																		
									<p>Severino “Sevy” Verna was so obsessed and afraid of death that as a kid he would take dead pigeons, cover them in cement and put them in a bucket. It is believed he did this in hopes of preserving, or perhaps resurrecting, the birds. This might have stemmed from his parents owning a funeral home, but it was an obsession that carried him throughout his whole life.</p>
<p>Verna is the supposed artist behind a series of mysterious, cryptic tiles appearing as early as the ‘80s originally in Philadelphia, then along the East Coast, to as far as South America that read: “TOYNBEE IDEA IN KUBRICK’S 2001 RESURRECT DEAD ON PLANET JUPITER.”</p>
<p>An investigative team, comprised of 2011 alumnus Colin Smith, Justin Duerr and Steve Weinik, searched for the answer in their 2011 documentary, “Resurrect Dead: The Mystery of the Toynbee Tiles,” which took about six years of thorough research to create.</p>
<p>“I figured it was some street art campaign, but it turns out it’s a mystery,” Smith said at a free screening of the documentary at The Reel on Nov. 8. “And over a couple years, you start thinking, ‘What do they mean?’ and ‘Why don’t we know who made them?’ And then at some point, you get extremely obsessed with that question, and it keeps you up at night and you seek out other people thinking that same thing.”</p>
<p>Duerr, who has been investigating this since 1994, described the tiles in his film as one of the Top 5 wildest things he’s ever seen in his life.</p>
<p>After hitting many dead ends in their search for an answer, Smith, Duerr and Weinik began getting clues from websites, letters and ordinary people who shared the same curiosity. They ended up having three strong leads: A 1983 Philadelphia Inquirer article, a play titled “Four A.M.” by David Mamet and a Philadelphia home address. All three connected to the philosopher Arnold Toynbee’s idea of resurrection, Stanley Kubrick’s “2001: A Space Odyssesy” and to one man: Verna.</p>
<p>“I think that they began because [Verna] had this idea that came to him in the library that seemed like he had found this way to beat death,” Smith said. “That humanity needs to figure a way to beat death, and [Verna] figured out a way to do it.”</p>
<p>Smith said Verna would desperately try to contact the media to share his idea, and they would laugh at him, leaving him embarrassed and bitter. He had to find a way to bring his message to the public and the tiles were the only way he knew how.</p>
<p>The proof of his hatred and resentment toward the media lies in various subtexts of the tiles that read such cryptic messages such as: “MURDER EVERY JOURNALIST! I BEG YOU.”</p>
<p>“There were moments where you get into the tiler’s head,” Smith said.</p>
<p>His resentment from others caused him to become a recluse of sorts, refusing to answer the door, phone calls and neighbors who had claimed he would do his grocery shopping at 2 a.m. or 3 a.m. This made the team’s journey increasingly difficult.</p>
<hr />
<p>Even though the three had no proof from Verna himself that he’s behind this, they remain confident because of their work.</p>
<p>“I was the most skeptical person working on the movie,” Smith said. “There would be periods of six months where we would run out of leads. Then suddenly, kind of magically, these new leads would come in. Just when we would get frustrated, almost unbearably, a new lead would come along.”</p>
<p>The biggest clue that gave Verna away was his car. The tiles seemed to appear in the middle of busy streets, and that’s because Verna’s car didn’t have floorboards, claimed neighbors, which allowed him to go essentially unnoticed.</p>
<p>“It was a case of putting together what we had,” Smith said.</p>
<p>The team has sent letters to Verna after the making of the documentary expressing their sincerity, interest and respect for him. They’ve also sent him a finished copy of the documentary, but none of them have ever gotten any sort of response.</p>
<p>Smith claims they’ve all seen Verna in the city, but no one has ever said anything. They now understand they can’t make him explain if he doesn’t want to. But Smith has a feeling he knows exactly who they are.</p>
<p>These messages can be spotted all over Philadelphia, from Chestnut Street to South Street. There was even one on Main Campus near Anderson and Gladfelter halls that was paved over, but remnants of it can still be seen.</p>
<p>It’s a strange phenomenon that’s appeared in the city, and the irony is that even though Verna wanted his message known, he made it very difficult for anyone to get any further information. The theater was crowded with Mosaics I and II classes, and when asked who had ever seen the tiles, few had raised their hands.</p>
<p>Though many students left as soon as their allotted class time was over, a few stayed for the Q&amp;A to ask questions not covered in the documentary, which was also shown in the 2011 Sundance Film Festival.</p>
<p>“[The documentary] was very interesting,” said Ani Soghomonian, a sophomore speech pathology major. “I’ve never noticed the tiles before, but now I’ll be on the lookout.”</p>
